# Ejercicio 5:
# Escribir un programa que pregunte al usuario por el número de horas trabajadas y el coste por hora. Después debe mostrar por pantalla la paga que le corresponde.
horas_trabajadas = int(input("Ingrese cuantas horas tiene trabajadas: "))
coste_por_hora  = int(input("Ingrese cuanto es el coste por hora: "))
print("La paga correspondiente es de: ",horas_trabajadas * coste_por_hora)
